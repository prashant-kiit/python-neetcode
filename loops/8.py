# for loop

for n in range(5):
    print(n)

print('---------')

for n in range(5, 10):
    print(n)

print('---------')

for n in range(5, 10, 2):
    print(n)

print('---------')

for n in range(5, 10, 10):
    print(n)

print('---------')

for n in range(5, 10, -1):
    print(n)

print('---------')

for n in range(10, 5, -2):
    print(n)

print('---------')

